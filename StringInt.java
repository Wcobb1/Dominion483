package dominionAgents;

public class StringInt {

	private String s;
	private int i;
	
	public StringInt(String s, int i) {
		super();
		this.s = s;
		this.i = i;
	}

	public String getString() {
		return s;
	}

	public void setString(String s) {
		this.s = s;
	}

	public int getInt() {
		return i;
	}

	public void setInt(int i) {
		this.i = i;
	}
	
}
